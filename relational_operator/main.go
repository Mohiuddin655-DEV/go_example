package main

import "fmt"

func main() {

	// Types of relational operators => >, <, ==, <=, >=, !=

	value := 5

	fmt.Println("Relational operator (>)  : ", value > 5)
	fmt.Println("Relational operator (<)  : ", value < 5)
	fmt.Println("Relational operator (==) : ", value == 5)
	fmt.Println("Relational operator (>=) : ", value >= 5)
	fmt.Println("Relational operator (<=) : ", value <= 5)
	fmt.Println("Relational operator (!=) : ", value != 5)

}
